package encap;

public class Person {
    private String name;
    private int age ;


    public void setName(String newName) {
        this.name = newName ;
    }

    public void setAge(int Newage) {

        this.age = Newage ;
    }

    public String getName() {

        return name ;
    }

    public int getAge() {

        return age ;
    }
}
